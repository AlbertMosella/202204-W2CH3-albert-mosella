let nombreUsuario;

const obtenerNombreUsuario = () => {
  nombreUsuario = prompt("Introduce tu nombre:");
  if (!nombreUsuario) {
    nombreUsuario = "Invitado";
    return alert("Hola Invitado y bienvenido a Pasapalabra!");
  } else {
    return alert(`Hola ${nombreUsuario} y bienvenido a Pasapalabra!`);
  }
};

let usuarioEnd = false;

const inicioRosco = () => {
  alert(
    "Instrucciones: si dejas el campo vacio o presionas cancelar el juego te volverá a preguntar la misma letra, si escribes 'pasapalabra', pasaras a la siguiente pregunta y si escribes 'end' finalizaras el juego"
  );
  let respuestaUsuario;
  do {
    for (let i = 0; i < pasapalabra.length; i++) {
      if (pasapalabra[i].estado === 0 && !usuarioEnd) {
        do {
          respuestaUsuario = prompt(pasapalabra[i].pregunta);
        } while (!respuestaUsuario);
        if (respuestaUsuario.toLowerCase() === pasapalabra[i].respuesta) {
          alert("Has acertado! Has ganado un punto!");
          pasapalabra[i].estado = 1;
        } else if (respuestaUsuario.toUpperCase() === "END") {
          usuarioEnd = true;
        } else if (respuestaUsuario.toUpperCase() === "PASAPALABRA") {
          alert("Has pasado palabra!");
        } else {
          alert(
            `Has fallado y has perdido un punto! La respuesta correcta era "${pasapalabra[i].respuesta}"!`
          );
          pasapalabra[i].estado = -1;
        }
      }
    }
  } while (!comprobarRosco() && !usuarioEnd);
  if (usuarioEnd) {
    alert(
      `Has finalizado el juego con un total de ${calcularPuntos()} puntos!`
    );
  }
  if (comprobarRosco()) {
    finalJuego();
  }
};

const comprobarRosco = () => {
  for (let i = 0; i < pasapalabra.length; i++) {
    if (pasapalabra[i].estado === 0) {
      return false;
    }
  }
  return true;
};

const finalJuego = () => {
  alert(`Has completado el rosco con un total de ${calcularPuntos()} puntos!`);
  let aciertosErrores = calcularAciertosyErrores();

  alert(
    `Has acertado un total de ${aciertosErrores[0]} letras y has fallado un total de ${aciertosErrores[1]} letras!`
  );
  mostrarRanking(aciertosErrores[0]);
};

const calcularPuntos = () => {
  let totalPuntos = 0;
  for (let i = 0; i < pasapalabra.length; i++) {
    totalPuntos += pasapalabra[i].estado;
  }
  return totalPuntos;
};

const calcularAciertosyErrores = () => {
  let totalAciertos = 0;
  let totalErrores = 0;
  for (let i = 0; i < pasapalabra.length; i++) {
    if (pasapalabra[i].estado === 1) {
      totalAciertos += 1;
    }
  }
  totalErrores = 27 - totalAciertos;
  let balanceAciertos = [totalAciertos, totalErrores];
  return balanceAciertos;
};

